package com.studyflow.domain.subject.enums;

/**
 * 수능(대학수학능력시험) 기준 8개 과목 대분류.
 *
 * <p>StudyFlow의 과목 체계는 이 8개 대분류를 최상위(부모 없는) 과목으로 둔다.
 * 각 분류는 화면에 노출할 한글 이름({@link #getDisplayName()})과,
 * AI 질문 시 해당 과목 특성에 맞춰 답변하도록 모델에 주입할 과목별 지침
 * ({@link #getTutorGuidance()})을 함께 가진다.</p>
 *
 * <p>이 enum이 8개 과목의 "단일 진실 원천(single source of truth)"이며,
 * 앱 기동 시 {@code subject} 테이블에 동일한 분류가 자동으로 시딩된다.</p>
 */
public enum SubjectCategory {

    KOREAN("국어", """
            국어 영역(독서·문학·화법과 작문·언어와 매체) 질문이다.
            지문 해석 문제는 근거가 되는 문장을 짚어 설명하고, 문법(언어) 문제는
            문법 요소(음운·단어·문장)를 개념부터 정리한 뒤 적용하라.
            문학은 시·소설의 표현 기법과 주제 의식을 함께 풀어 설명하라."""),

    ENGLISH("영어", """
            영어 영역(독해·어법·어휘) 질문이다.
            영어 지문이 포함되면 핵심 문장은 우리말 해석을 함께 제시하고,
            빈칸·주제·요지 추론은 단서가 되는 표현을 근거로 논리를 보여라.
            중요 어휘와 숙어는 따로 정리해 학습에 도움이 되게 하라."""),

    MATH("수학", """
            수학 영역 질문이다.
            개념·공식을 먼저 짚고, 풀이는 한 단계씩 빠짐없이 전개하라.
            수식은 한 줄에 하나씩 보기 좋게 작성하고, 마지막에 검산 또는
            답이 타당한지 확인하는 과정을 덧붙여라."""),

    SOCIAL_STUDIES("사회탐구", """
            사회탐구 영역(생활과 윤리·사회·문화·한국지리·세계사·정치와 법 등) 질문이다.
            핵심 개념의 정의를 명확히 하고, 구체적인 사례나 자료(표·그래프)와 연결해 설명하라.
            헷갈리기 쉬운 개념은 비교해서 구분해 주어라."""),

    SCIENCE("과학탐구", """
            과학탐구 영역(물리학·화학·생명과학·지구과학) 질문이다.
            원리와 공식이 "왜" 그런지 의미부터 설명하고, 단위와 부호를 정확히 다뤄라.
            그래프·실험 결과 해석 문제는 변수 관계를 단계적으로 짚어 설명하라."""),

    VOCATIONAL("직업탐구", """
            직업탐구 영역(상업 경제·공업 일반·농업 기초·수산 해운·인간 발달 등 전문 교과) 질문이다.
            실무 중심 개념과 전문 용어의 뜻을 쉽게 풀어 설명하고,
            현장에서 어떻게 쓰이는지 예시를 들어 이해를 도와라."""),

    KOREAN_HISTORY("한국사", """
            한국사 영역 질문이다.
            사건은 시대 흐름(연표) 속에서 원인과 결과를 연결해 설명하고,
            사료·지도·유물 자료 해석은 그 자료가 어느 시기·맥락의 것인지 근거를 들어 판단하라."""),

    SECOND_LANGUAGE("제2외국어", """
            제2외국어/한문 영역(일본어·중국어·독일어·프랑스어·한문 등) 질문이다.
            어휘·문법·회화 표현은 발음(또는 독음) 표기를 함께 제시하고,
            우리말 뜻과 쓰임새를 예문으로 보여 주어라.""");

    private final String displayName;
    private final String tutorGuidance;

    SubjectCategory(String displayName, String tutorGuidance) {
        this.displayName = displayName;
        this.tutorGuidance = tutorGuidance;
    }

    /** 화면에 노출되는 과목 한글 이름. */
    public String getDisplayName() {
        return displayName;
    }

    /** AI에게 주입할 과목별 답변 지침(시스템 프롬프트에 덧붙여 사용). */
    public String getTutorGuidance() {
        return tutorGuidance;
    }
}
